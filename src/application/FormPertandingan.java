    public void getDataField(){
        String sql = "SELECT fieldname from fieldowner";
        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                tempat_pertandingan.addItem(rs.getString("fieldname"));
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

