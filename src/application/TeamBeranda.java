    private void tb_teamSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_teamSearchMouseClicked
        // TODO add your handling code here:
        int row = tb_teamSearch.getSelectedRow();
        String idteamClickTable = (tb_teamSearch.getModel().getValueAt(row,0).toString());
        
        try{
            String sql = "select * from team where id_team = '"+idteamClickTable+"' ";
            pst = conn.prepareStatement(sql);            
            rs = pst.executeQuery();
            
            
            //Set Inputan dengan yang diklik ditable
            if(rs.next()){
                id_teamSearch = rs.getString("id_team");
                btn_visitTeam.setVisible(true);
            }
                    
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{
                rs.close();
                pst.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_tb_teamSearchMouseClicked

