    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try{
//            tb_search.setModel(new DefaultTableModel());
            String sql = "select * from team WHERE id_team = '"+id_team+"'";
            pst = conn.prepareStatement(sql);
                      
            rs = pst.executeQuery();
            if(rs.next()){
                String teamName = rs.getString("teamname");
                String contactNo = rs.getString("contact");
                String domisili = rs.getString("region");
                label_teamName.setText(teamName);
                label_teamPhone.setText(contactNo);
                label_teamDomisili.setText(domisili);
                
                
                byte[] imagedata = rs.getBytes("photo");
                viewimage = new ImageIcon(imagedata);
                imageLabel.setIcon(viewimage);
            }else{
                JOptionPane.showMessageDialog(null, "Team Name didn't show");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error : "+ex.getMessage());
        }
    }//GEN-LAST:event_formWindowOpened
    
